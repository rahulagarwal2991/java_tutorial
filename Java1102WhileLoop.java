/**
 * Java While Loop: 
 * 
 * Syntax: 
 * 
 * while (condition){
 *  statement
 * }
 */

 // WAP to print value from 1 to 5

 public class Java1102WhileLoop{
     
    public static void main(String[] args) {

        int i = 1;
        while(i <=5){
            System.out.println(i);
            i++;
        }
    }
 }