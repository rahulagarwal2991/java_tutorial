/**
 * This file is used for showing demonstartion of
 * Arithmetic operator
 * 
 */
public class airthmetic_operator {
    public static void main(String[] args) {
        // + - * / % ++ --

        int a = 10;
        int b = 20;
        int c = a+b;
        System.out.println(c);
        
        c = b-a;
        System.out.println(c);

        c = a*b;
        System.out.println(c);

        c = 10/3;
        System.out.println(c);// quotient

        c = 10%3;
        System.out.println(c);//remainder
    }
}
