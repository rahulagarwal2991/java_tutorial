/**
 * strings are used for storing characters or combination of char 
 * which are surrounded by double quotes.
 * Syntax: 
 * String variable_name = "some_value";
 * 
 * length() method
 */

 class Java3301Strings{
     public static void main(String[] args) {
         String name = "Utkarsh";
        System.out.println(name.length());// 7
        System.out.println(name.toUpperCase());//UTKARSH
        System.out.println(name.toLowerCase());//utkarsh

     }
 }